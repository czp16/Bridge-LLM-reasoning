from sverl import DataProto
import torch

import re


def gsm8k_extract_solution(solution_str, method='strict'):
    assert method in ['strict', 'flexible']

    if method == 'strict':
        # this also tests the formatting of the model
        solution = re.search("#### (\\-?[0-9\\.\\,]+)", solution_str)
        if solution is None:
            final_answer = None
        else:
            final_answer = solution.group(0)
            final_answer = final_answer.split('#### ')[1].replace(',', '').replace('$', '')
    elif method == 'flexible':
        answer = re.findall("(\\-?[0-9\\.\\,]+)", solution_str)
        final_answer = None
        if len(answer) == 0:
            # no reward is there is no answer
            pass
        else:
            invalid_str = ['', '.']
            # find the last number that is not '.'
            for final_answer in reversed(answer):
                if final_answer not in invalid_str:
                    break
    return final_answer


def gsm8k_compute_score(solution_str, ground_truth, method='strict', format_score=0., score=1.):
    """The scoring function for GSM8k.

    Reference: Trung, Luong, et al. "Reft: Reasoning with reinforced fine-tuning." Proceedings of the 62nd Annual Meeting of the Association for Computational Linguistics (Volume 1: Long Papers). 2024.

    Args:
        solution_str: the solution text
        ground_truth: the ground truth
        method: the method to extract the solution, choices are 'strict' and 'flexible'
        format_score: the score for the format
        score: the score for the correct answer
    """
    answer = gsm8k_extract_solution(solution_str=solution_str, method=method)
    if answer is None:
        return 0
    else:
        if answer == ground_truth:
            return score
        else:
            return format_score
        
def igsm_extract_solution(solution_str):
    # The regex looks for the literal "\boxed{" followed by any characters that are not "}" until a "}" is found.
    # note here we only suppose the solution in the box is an integer, this may not be applicable to other cases,
    # e.g., the solution is a fraction
    pattern = r'\\boxed\{([^}]*)\}'
    match = re.search(pattern, solution_str)
    if match:
        return match.group(1)
    return None

def igsm_format_reward(completion, prefix: str="<think>"):
    """
    Reward function that checks if the reasoning process is enclosed within <think> and </think> tags, 
    while the final answer is enclosed within <answer> and </answer> tags.
    Here we add the <think> tag to the text because we include the tag in query template. You need to
    remove it if you don't include the tag in the query template.
    """
    pattern = r"^<think>.*?</think>\s*<answer>.*?</answer>$"
    return 1.0 if re.match(pattern, prefix + completion, re.DOTALL | re.MULTILINE) else 0.0

def igsm_compute_score(completions, ground_truths, format_reward=0.0):
    scores = []
    for c, gt in zip(completions, ground_truths):
        answer = igsm_extract_solution(solution_str=c)
        if answer is None:
            score = 0
        else:
            score = 1 if answer == str(gt) else 0
        if format_reward > 0.0:
            score += format_reward * igsm_format_reward(c)
        scores.append(score)
    return scores


